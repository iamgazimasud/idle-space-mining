  // ---------------------------------------------------------- tabs
  buildTabs() {
    const nav = document.getElementById('tabs');
    nav.innerHTML = this.tabs.map(t =>
      `<button class="tab-btn" id="tab-${t.id}"><span>${t.icon}</span><span class="tab-name">${t.name}</span></button>`
    ).join('');
    this.tabs.forEach(t => {
      document.getElementById('tab-' + t.id).onclick = () => this.showTab(t.id);
    });
  },

  showTab(id) {
    this.activeTab = id;
    this.tabs.forEach(t =>
      document.getElementById('tab-' + t.id).classList.toggle('active', t.id === id));
    this.renderPanel();
  },

  // ---------------------------------------------------------- resources sidebar
  buildResourcePanel() {
    // structure only; rows are (re)built in updateResources when the set changes
    this._resSet = '';
  },

  updateResources() {
    const holder = document.getElementById('resources-panel');
    const visible = DATA.resources.filter(r => (S.resources[r.id] || 0) > 0.01);
    const key = visible.map(r => r.id).join(',') + (S.research.autosell ? '|a' : '');
    if (key !== this._resSet) {
      this._resSet = key;
      let html = `<div class="panel-title">📦 Cargo Hold ${S.research.autosell ? '<span class="badge-auto">AUTO-SELL</span>' : ''}</div>`;
      if (!visible.length) html += `<div class="res-empty">Click the planet to start mining!</div>`;
      for (const r of visible) {
        html += `<div class="res-row" data-tip="${r.name} — worth ${fmt(r.value)} credits each (${r.tier})">
          <span class="res-dot" style="background:${r.color}"></span>
          <span class="res-name">${r.name}</span>
          <span class="res-amt" id="res-${r.id}">0</span>
        </div>`;
      }
      html += `<button class="btn btn-sell" id="btn-sell">Sell All <span id="sell-val"></span></button>`;
      holder.innerHTML = html;
      const btn = document.getElementById('btn-sell');
      if (btn) btn.onclick = () => {
        const v = Game.sellAll();
        if (v > 0) {
          Sound.play('sell');
          const rect = document.getElementById('scene').getBoundingClientRect();
          Render.floatText('+' + fmt(v) + ' cr', rect.width / 2, rect.height * 0.25, '#ffd54a');
        }
      };
    }
    for (const r of visible) {
      const el = document.getElementById('res-' + r.id);
      if (el) el.textContent = fmt(S.resources[r.id] || 0);
    }
    const sv = document.getElementById('sell-val');
    if (sv) sv.textContent = '(+' + fmt(Game.sellValue()) + ')';
  },

  updatePlanetInfo() {
    const el = document.getElementById('planet-info');
    const p = DATA.planetMap[S.currentPlanet];
    if (this._piKey !== p.id) {
      this._piKey = p.id;
      const chips = Object.entries(p.dist).map(([rid, w]) => {
        const r = DATA.resourceMap[rid];
        return `<span class="chip" style="border-color:${r.color}55" data-tip="${r.name}: ${Math.round(w * 100)}% of mined ore">
          <span class="res-dot" style="background:${r.color}"></span>${Math.round(w * 100)}%</span>`;
      }).join('');
      el.innerHTML = `<div class="pi-name">${p.name} <span class="pi-mult">×${fmt(p.mult)} mining</span></div>
        <div class="pi-chips">${chips}</div>
        <div class="pi-rates"><span data-tip="Ore mined automatically per second" id="pi-ore"></span>
        <span data-tip="Ore per click" id="pi-click"></span></div>`;
    }
    document.getElementById('pi-ore').textContent = '⛏️ ' + fmt(Game.oreRate()) + ' ore/s';
    document.getElementById('pi-click').textContent = '👆 ' + fmt(Game.clickPower()) + '/click';
  },

  updateEventBanner() {
    const el = document.getElementById('event-banner');
    if (S.event) {
      const def = DATA.eventMap[S.event.id];
      const left = Math.max(0, (S.event.endsAt - Date.now()) / 1000);
      el.style.display = 'flex';
      el.className = 'event-banner ' + (def.good ? 'ev-good' : 'ev-bad');
      el.innerHTML = `<span>${def.icon} <b>${def.name}</b> — ${def.desc}</span><span class="ev-timer">${Math.ceil(left)}s</span>`;
    } else {
      el.style.display = 'none';
    }
  },

  // ---------------------------------------------------------- generic buy row
  buyRow(parent, opts) {
    const row = document.createElement('div');
    row.className = 'buy-row';
    if (opts.tip) row.dataset.tip = opts.tip;
    row.innerHTML = `
      <div class="br-icon">${opts.icon}</div>
      <div class="br-body">
        <div class="br-name">${opts.name} <span class="br-count"></span></div>
        <div class="br-desc">${opts.desc || ''}</div>
      </div>
      <button class="btn btn-buy"><span class="bb-label"></span><span class="bb-cost"></span></button>`;
    const countEl = row.querySelector('.br-count');
    const btn = row.querySelector('.btn-buy');
    const labelEl = row.querySelector('.bb-label');
    const costEl = row.querySelector('.bb-cost');
    btn.onclick = opts.onBuy;
    parent.appendChild(row);
    this.updaters.push(() => {
      countEl.textContent = opts.countFn ? opts.countFn() : '';
      labelEl.textContent = opts.labelFn ? opts.labelFn() : 'Buy';
      costEl.textContent = opts.costFn();
      btn.disabled = !opts.canFn();
      if (opts.descFn) row.querySelector('.br-desc').innerHTML = opts.descFn();
    });
    return row;
  },

  // ---------------------------------------------------------- panels
  renderPanel() {
    this.updaters = [];
    const panel = document.getElementById('panel');
    panel.innerHTML = '';
    const fn = this['panel_' + this.activeTab];
    if (fn) fn.call(this, panel);
    this._dirty = false;
    this.runUpdaters();
  },

  runUpdaters() {
    for (const u of this.updaters) { try { u(); } catch (e) { /* row removed */ } }
  },

  section(panel, title, sub) {
    const d = document.createElement('div');
    d.className = 'panel-section';
    d.innerHTML = `<div class="panel-title">${title}</div>` + (sub ? `<div class="panel-sub">${sub}</div>` : '');
    panel.appendChild(d);
    return d;
  },

  panel_mine(panel) {
    const s1 = this.section(panel, '👆 Manual Mining');
    this.buyRow(s1, {
      icon: '🔧', name: 'Drill Power',
      desc: 'Upgrade your manual drill. +1 base ore per click.',
      tip: 'Click power = (1 + drill level) × all mining multipliers',
      countFn: () => 'Lv ' + S.drill,
      labelFn: () => 'Upgrade',
      costFn: () => fmt(Game.drillCost()) + ' cr',
      canFn: () => S.credits >= Game.drillCost(),
      onBuy: () => Game.buyDrill(),
    });

    const s2 = this.section(panel, '🛸 Automated Miners', 'Miners produce ore every second on your current planet.');
    DATA.miners.forEach((m, i) => {
      // A tier appears once you own at least one of the previous tier
      if (i > 0 && !(S.miners[DATA.miners[i - 1].id] > 0) && !(S.miners[m.id] > 0)) return;
      this.buyRow(s2, {
        icon: m.icon, name: m.name, desc: m.desc,
        tip: `${m.name}<br>Base production: ${fmt(m.prod)} ore/s each<br>Energy use: ${m.energy}/each`,
        countFn: () => {
          const c = S.miners[m.id] || 0;
          return c > 0 ? '×' + fmtInt(c) + ' · ' + fmt(c * m.prod * Game.miningMult() * Game.energyEff()) + '/s' : '';
        },
        labelFn: () => Game.buyAmount === 'max' ? 'Max' : 'x' + Game.buyAmount,
        costFn: () => fmt(Game.buyPlan(m.cost, DATA.minerCostMult, S.miners[m.id] || 0).cost) + ' cr',
        canFn: () => {
          const plan = Game.buyPlan(m.cost, DATA.minerCostMult, S.miners[m.id] || 0);
          return S.credits >= plan.cost && plan.n >= 1;
        },
        onBuy: () => Game.buyMiner(m.id),
      });
    });
  },

