  // ---------------------------------------------------------- toasts & modals
  toast(msg, cls, dur) {
    const holder = document.getElementById('toasts');
    const el = document.createElement('div');
    el.className = 'toast ' + (cls || 'info');
    el.innerHTML = msg;
    holder.appendChild(el);
    while (holder.children.length > 5) holder.removeChild(holder.firstChild);
    setTimeout(() => el.classList.add('show'), 10);
    setTimeout(() => {
      el.classList.remove('show');
      setTimeout(() => el.remove(), 400);
    }, dur || 4000);
  },

  toastOnceKey(key, msg) {
    if (this._toastKeys[key]) return;
    this._toastKeys[key] = 1;
    this.toast(msg, 'info');
  },

  modal(html) {
    const root = document.getElementById('modal-root');
    root.innerHTML = `<div class="modal-back"><div class="modal">${html}</div></div>`;
    root.querySelector('.modal-back').onclick = e => { if (e.target.classList.contains('modal-back')) this.closeModal(); };
  },

  closeModal() {
    document.getElementById('modal-root').innerHTML = '';
  },

